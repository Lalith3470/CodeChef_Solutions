t = int(input())

for i in range(t):
    n = int(input())
    
    count = 0
    for k in range(n):
        s = input()
    
    for j in range(n):
        d = input()
        
    a = s.split()
    b = d.split()
        
    for l in range(n):
        if a[l] == b[l]:
            count += 1
    print(count)
